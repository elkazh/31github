package org.example;

public class Variables {
    public static void main(String[] args) {
        int myInt = 558;
        short myShort = 3266;
        long myLong = 28993555;

        double myDouble = 235.36;
        float myFloat = 6348.45f;

        char myChar = 'h';
        boolean myBoolean = false;

        byte myByte = -100;

        System.out.println(myInt + ", "+ myShort + ", "+ myLong);
        System.out.println(myDouble + ", "+ myFloat);
        System.out.println(myChar + ", " + myBoolean);
        System.out.println(myByte);
    }
}